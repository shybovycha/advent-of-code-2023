fn is_match(input: &str, breaks: Vec<usize>) -> bool {
    let mut result = Vec::new();
    let mut n = 0;

    for ch in input.chars() {
        if ch == '#' {
            n += 1;
        } else if n > 0 {
            result.push(n);
            n = 0;
        }
    }

    if n > 0 {
        result.push(n);
    }

    result.len() == breaks.len() && result.iter().enumerate().all(|(idx, elt)| breaks.get(idx).map(|e| e == elt).unwrap_or(false))
}

fn solve_for_one(input: &str, breaks: Vec<usize>) -> usize {
    let mut variants = HashSet::new();

    generate_str(input.to_string(), 0, &mut variants);

    // println!("{} => {} variants", input, variants.len());

    let valid_variants = variants
        .iter()
        .filter(|v| is_match(v, (*breaks).to_vec()))
        .collect::<Vec<_>>();

    // println!("{} / {} => {} variants, {} valid: {}", input, breaks.iter().map(|c| c.to_string()).collect::<Vec<_>>().join(","), variants.len(), valid_variants.len(), valid_variants.iter().map(|s| s.to_string()).collect::<Vec<_>>().join(","));
    println!("{} / {} => {} variants, {} valid", input, breaks.iter().map(|c| c.to_string()).collect::<Vec<_>>().join(","), variants.len(), valid_variants.len());

    valid_variants.len()
}

