fn solve(field: &str) -> usize {
    let lines = field.lines().collect::<Vec<_>>();

    let rows = lines.len();
    let cols = lines.get(0).unwrap().len();

    let mut pos = lines.iter().map(|l| l.chars().collect::<Vec<_>>()).collect::<Vec<_>>(); //(0..rows).map(|row| (0..cols).map(|col| lines.get(row).unwrap().chars().nth(col).unwrap()).collect::<Vec<_>>()).collect::<Vec<_>>();

    let mut cache = HashMap::new();

    let mut fl = true;

    let mut i = 0;
    while i < 1000000000 {
        roll_north(&mut pos, rows, cols);
        roll_west(&mut pos, rows, cols);
        roll_south(&mut pos, rows, cols);
        roll_east(&mut pos, rows, cols);

        if fl {
            let key = pos.clone();

            if cache.contains_key(&key) {
                let cycle = i - cache.get(&key).unwrap();
                let remaining_cycles = (1000000000 - i) / cycle;
                i += cycle * remaining_cycles;
                fl = false;
            } else {
                cache.insert(key, i);
            }
        }

        i += 1;
    }

    get_north_load(&pos, rows, cols)
}

fn field_to_str(pos: &Vec<Vec<char>>) -> String {
    pos.iter().map(|r| r.iter().collect::<String>()).collect::<Vec<_>>().join("\n")
}

fn get_north_load(pos: &Vec<Vec<char>>, rows: usize, cols: usize) -> usize {
    let mut res = 0;

    for row in 0..rows {
        for col in 0..cols {
            let ch = *pos.get(row).unwrap().get(col).unwrap();

            if ch == 'O' {
                res += rows - row;
            }
        }
    }

    res
}

fn roll_north(pos: &mut Vec<Vec<char>>, rows: usize, cols: usize) {
    for col in 0..cols {
        let mut next_pos = 0;

        for row in 0..rows {
            let ch = *pos.get(row).unwrap().get(col).unwrap();

            if ch == 'O' {
                let prev_row_acc = pos.get_mut(row).unwrap();
                prev_row_acc[col] = '.';

                let row_acc = pos.get_mut(next_pos).unwrap();
                row_acc[col] = 'O';

                next_pos += 1;
            } else if ch == '#' {
                next_pos = row + 1;
            }
        }
    }
}

fn roll_south(pos: &mut Vec<Vec<char>>, rows: usize, cols: usize) {
    for col in 0..cols {
        let mut next_pos = rows - 1;

        for row in (0..rows).rev() {
            let ch = *pos.get(row).unwrap().get(col).unwrap();

            if ch == 'O' {
                let prev_row_acc = pos.get_mut(row).unwrap();
                prev_row_acc[col] = '.';

                let row_acc = pos.get_mut(next_pos).unwrap();
                row_acc[col] = 'O';

                if next_pos > 0 {
                    next_pos -= 1;
                }
            } else if ch == '#' {
                if row > 0 {
                    next_pos = row - 1;
                }
            }
        }
    }
}

fn roll_west(pos: &mut Vec<Vec<char>>, rows: usize, cols: usize) {
    for row in 0..rows {
        let mut next_pos = 0;

        for col in 0..cols {
            let ch = *pos.get(row).unwrap().get(col).unwrap();

            if ch == 'O' {
                let row_acc = pos.get_mut(row).unwrap();
                row_acc[col] = '.';
                row_acc[next_pos] = 'O';

                next_pos += 1;
            } else if ch == '#' {
                next_pos = col + 1;
            }
        }
    }
}

fn roll_east(pos: &mut Vec<Vec<char>>, rows: usize, cols: usize) {
    for row in 0..rows {
        let mut next_pos = cols - 1;

        for col in (0..cols).rev() {
            let ch = *pos.get(row).unwrap().get(col).unwrap();

            if ch == 'O' {
                let row_acc = pos.get_mut(row).unwrap();
                row_acc[col] = '.';
                row_acc[next_pos] = 'O';

                if next_pos > 0 {
                    next_pos -= 1;
                }
            } else if ch == '#' {
                if col > 0 {
                    next_pos = col - 1;
                }
            }
        }
    }
}
